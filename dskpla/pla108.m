% 《实用大众线性代数（MATLAB版）》例题1.8程序pla108
% 三次多项式插值计算 
% 陈怀琛编著，西安电子科技大学出版社，2014年7月
%
clear, close all
A=[1,0,0,0;1,1,1,1;1,2,4,8;1,3,9,27], 
b=[3;0;-1;6],                           % 给系数矩阵A,b赋值
U0=rref([A,b])
plot([0;1;2;3],b,'*'),hold on,grid on   % 画出插值点
t=0:0.1:3.5;                            % 给出较密的横坐标点       
p=3-2*t-2*t.^2+t.^3                     % 求这些点上的多项式值
plot(t,p,'r');                          % 画出插值曲线
plot(1.5,-1.125,'s')                    % 画出给定点
set(gcf,'color','w')
